# o sep (é separador) e o end (é o final) são parâmetros opcionais da função print
print('Hello', 'World', sep='-') # o sep é o separador entre os argumentos, por defeito é um espaço
print('Hello', 'World', end='!') # o end é o final da linha se não mudar ele pula de linha por defeito
print('Hello', 'World', sep='-', end='!') # o sep é o separador entre os argumentos e o end